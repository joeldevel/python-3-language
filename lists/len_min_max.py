# some useful functionalities

print("### len() gives you how many elements a sequence has")
print("len('this is a sentence') = ", len("this is a sentence"))
print("len(['a','b','c']) = ", len(['a','b','c']))
print()

print("### min() gives you the minimum element in a sequence")
print("min('xba') = ", min('xba'))
print("min([100,2,4,6,1,3]) = ", min([100, 2, 4, 6, 1, 3]))
print()

print("### max() gives you the maximum element in a sequence")
print("max('xba') = ", max('xba'))
print("max([100,2,4,6,1,3]) = ", max([100, 2, 4, 6, 1, 3]))
