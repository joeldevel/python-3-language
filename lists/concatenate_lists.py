# lists can be concatenated using '+'
print("~~~~ lists can be concatenated using '+' ~~~~")

list_a = ["john", "juan", "giovanni"]
list_b = ["joseph", "jose", "pepe"]
list_c = [1, 2, 3]

print("list_a =", list_a)
print("list_b =", list_b)
print("list_c =", list_c)
print("list_a + list_b =", list_a + list_b)
print("list_a + list_b + list_c = ", list_a + list_b + list_c)
